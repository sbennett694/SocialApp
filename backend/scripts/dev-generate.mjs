import { createInterface } from "node:readline/promises";
import { stdin as input, stdout as output } from "node:process";

const command = (process.argv[2] || "help").toLowerCase();
const args = process.argv.slice(3);
const baseUrl = process.env.SOCIALAPP_LOCAL_API_URL || "http://127.0.0.1:3001";

function getArg(name, fallback) {
  const exact = `--${name}`;
  const withValue = args.find((entry) => entry.startsWith(`${exact}=`));
  if (withValue) return withValue.slice(exact.length + 1);
  const index = args.findIndex((entry) => entry === exact);
  if (index >= 0) return args[index + 1] ?? fallback;
  return fallback;
}

function getIntArg(name, fallback) {
  const raw = getArg(name, undefined);
  if (!raw) return fallback;
  const parsed = Number(raw);
  if (!Number.isInteger(parsed) || parsed <= 0) return fallback;
  return parsed;
}

function getDelayMs() {
  const raw = getArg("delay-ms", "0");
  const parsed = Number(raw);
  if (!Number.isFinite(parsed) || parsed < 0) return 0;
  return parsed;
}

function sleep(ms) {
  if (!ms) return Promise.resolve();
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function promptCount(defaultCount) {
  if (!process.stdin.isTTY) return defaultCount;
  const rl = createInterface({ input, output });
  try {
    const value = (await rl.question(`How many items? (default ${defaultCount}): `)).trim();
    if (!value) return defaultCount;
    const parsed = Number(value);
    if (!Number.isInteger(parsed) || parsed <= 0) return defaultCount;
    return parsed;
  } finally {
    rl.close();
  }
}

async function request(path, init = {}) {
  let response;
  try {
    response = await fetch(`${baseUrl}${path}`, init);
  } catch (error) {
    throw new Error(
      `Cannot reach local API at ${baseUrl}. Start backend with 'npm --prefix backend run local-api' or set SOCIALAPP_LOCAL_API_URL. (${error?.message ?? "fetch failed"})`
    );
  }
  const payload = await response.json().catch(() => ({}));
  if (!response.ok) {
    throw new Error(payload?.message || `Request failed (${response.status}) for ${path}`);
  }
  return payload;
}

async function ensureBackendHealthy() {
  await request("/health");
}

function nextSequence(existingLabels, prefix) {
  const normalizedPrefix = prefix.trim().toLowerCase();
  let max = 0;
  for (const label of existingLabels) {
    const lower = String(label ?? "").trim().toLowerCase();
    if (!lower.startsWith(normalizedPrefix)) continue;
    const match = lower.match(/(\d+)\s*$/);
    if (!match) continue;
    const value = Number(match[1]);
    if (Number.isFinite(value)) {
      max = Math.max(max, value);
    }
  }
  return max + 1;
}

async function getDefaultCategoryId() {
  const payload = await request("/categories");
  const first = payload?.categories?.find((entry) => entry?.isActive) ?? payload?.categories?.[0];
  if (!first?.id) {
    throw new Error("No category available from /categories.");
  }
  return first.id;
}

async function runCreateClubs() {
  const actor = getArg("actor", "alex");
  const categoryId = getArg("category-id", await getDefaultCategoryId());
  const prefix = getArg("prefix", `${actor} Test Club`);
  const requestedCount = await promptCount(getIntArg("count", 2));
  const count = Math.max(2, requestedCount);
  const clubs = await request("/clubs");
  const start = nextSequence(clubs.map((club) => club.name), prefix);
  const created = [];
  for (let index = 0; index < count; index += 1) {
    const name = `${prefix} ${start + index}`;
    const isPublic = index !== 1;
    const club = await request("/clubs", {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ ownerId: actor, categoryId, name, isPublic })
    });
    created.push(club);
  }
  return {
    command: "create-clubs",
    actor,
    requestedCount,
    count,
    visibilityRule: "First club public, second club private, additional clubs public.",
    created
  };
}

async function runCreateProjects() {
  const actor = getArg("actor", "alex");
  const categoryId = getArg("category-id", await getDefaultCategoryId());
  const prefix = getArg("prefix", `${actor} Test Project`);
  const count = await promptCount(getIntArg("count", 1));
  const projects = await request(`/projects?ownerId=${encodeURIComponent(actor)}`);
  const start = nextSequence(projects.map((project) => project.title), prefix);
  const created = [];
  for (let index = 0; index < count; index += 1) {
    const title = `${prefix} ${start + index}`;
    const project = await request("/projects", {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ ownerId: actor, createdBy: actor, categoryId, title, description: `Generated by dev-generate` })
    });
    created.push(project);
  }
  return { command: "create-projects", actor, count, created };
}

async function resolveProjectId(actor) {
  const fromArg = getArg("project-id", undefined);
  if (fromArg) return fromArg;
  const projects = await request(`/projects?ownerId=${encodeURIComponent(actor)}`);
  const first = projects?.[0];
  if (!first?.id) throw new Error("No project found. Pass --project-id or run create-projects first.");
  return first.id;
}

async function runCreateMilestones() {
  const actor = getArg("actor", "alex");
  const projectId = await resolveProjectId(actor);
  const prefix = getArg("prefix", `${actor} Test Milestone`);
  const count = await promptCount(getIntArg("count", 1));
  const milestones = await request(`/projects/${encodeURIComponent(projectId)}/milestones`);
  const start = nextSequence(milestones.map((milestone) => milestone.title), prefix);
  const created = [];
  for (let index = 0; index < count; index += 1) {
    const title = `${prefix} ${start + index}`;
    const milestone = await request(`/projects/${encodeURIComponent(projectId)}/milestones`, {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ actorId: actor, title })
    });
    created.push(milestone);
  }
  return { command: "create-milestones", actor, projectId, count, created };
}

async function runCreateTasks() {
  const actor = getArg("actor", "alex");
  const projectId = await resolveProjectId(actor);
  const milestones = await request(`/projects/${encodeURIComponent(projectId)}/milestones`);
  const milestoneId = getArg("milestone-id", milestones?.[0]?.id);
  if (!milestoneId) {
    throw new Error("No milestone found. Pass --milestone-id or run create-milestones first.");
  }
  const targetMilestone = milestones.find((milestone) => milestone.id === milestoneId);
  const prefix = getArg("prefix", `${actor} Test Task`);
  const count = await promptCount(getIntArg("count", 1));
  const start = nextSequence((targetMilestone?.tasks ?? []).map((task) => task.text), prefix);
  const created = [];
  for (let index = 0; index < count; index += 1) {
    const text = `${prefix} ${start + index} on ${targetMilestone?.title ?? milestoneId}`;
    const task = await request(
      `/projects/${encodeURIComponent(projectId)}/milestones/${encodeURIComponent(milestoneId)}/tasks`,
      {
        method: "POST",
        headers: { "content-type": "application/json" },
        body: JSON.stringify({ actorId: actor, text })
      }
    );
    created.push(task);
  }
  return { command: "create-tasks", actor, projectId, milestoneId, count, created };
}

async function runCreateComments() {
  const actor = getArg("actor", "jamie");
  const postIdArg = getArg("post-id", undefined);
  const postAuthor = getArg("post-author", "alex");
  const feed = await request(`/posts?viewerId=${encodeURIComponent(actor)}`);
  const targetPost = postIdArg
    ? feed.find((post) => post.postId === postIdArg)
    : feed.find((post) => post.userId === postAuthor) ?? feed[0];
  if (!targetPost?.postId) {
    throw new Error("No visible post found for comment creation. Pass --post-id or create a post first.");
  }

  const prefix = getArg("prefix", `${actor} test comment`);
  const count = await promptCount(getIntArg("count", 1));
  const delayMs = getDelayMs();
  const threadType = getArg("thread-type", "COMMENTS").toUpperCase();
  const existing = await request(`/posts/${encodeURIComponent(targetPost.postId)}/comments?threadType=${encodeURIComponent(threadType)}`);
  const start = nextSequence(existing.map((comment) => comment.textContent), prefix);
  const created = [];
  for (let index = 0; index < count; index += 1) {
    const textContent = `${prefix} ${start + index}`;
    const comment = await request("/comments", {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ postId: targetPost.postId, authorId: actor, threadType, textContent })
    });
    created.push(comment);
    await sleep(delayMs);
  }

  return { command: "create-comments", actor, postId: targetPost.postId, postOwner: targetPost.userId, count, created };
}

async function runScenarioCommentNav() {
  const postAuthor = getArg("post-author", "alex");
  const commenter = getArg("commenter", "jamie");
  const visibility = getArg("visibility", "FOLLOWERS").toUpperCase();
  const postText = getArg("post-text", `${postAuthor} Test Notification Post`);
  const commentPrefix = getArg("comment-prefix", `${commenter} Test Notification Comment`);
  const requestedPostCount = getIntArg("post-count", 2);
  const postCount = Math.max(2, requestedPostCount);
  const commentCount = await promptCount(getIntArg("count", 1));
  const delayMs = getDelayMs();
  const scenarios = [];

  for (let postIndex = 0; postIndex < postCount; postIndex += 1) {
    const postVisibility = postIndex === 0 ? "PUBLIC" : postIndex === 1 ? visibility : "PUBLIC";
    const post = await request("/posts", {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify({
        userId: postAuthor,
        text: `${postText} ${postIndex + 1}`,
        visibility: postVisibility
      })
    });

    const createdComments = [];
    for (let index = 0; index < commentCount; index += 1) {
      const comment = await request("/comments", {
        method: "POST",
        headers: { "content-type": "application/json" },
        body: JSON.stringify({
          postId: post.postId,
          authorId: commenter,
          threadType: "COMMENTS",
          textContent: `${commentPrefix} ${postIndex + 1}.${index + 1}`
        })
      });
      createdComments.push(comment);
      await sleep(delayMs);
    }

    scenarios.push({
      visibility: postVisibility,
      postId: post.postId,
      commentIds: createdComments.map((entry) => entry.id)
    });
  }

  return {
    command: "scenario-comment-nav",
    postAuthor,
    commenter,
    requestedPostCount,
    postCount,
    visibilityRule: "First post PUBLIC, second post uses --visibility (default FOLLOWERS), additional posts PUBLIC.",
    scenarios,
    note: `Open Notifications as ${postAuthor} to test comment navigation.`
  };
}

const handlers = {
  "create-clubs": runCreateClubs,
  "create-projects": runCreateProjects,
  "create-milestones": runCreateMilestones,
  "create-tasks": runCreateTasks,
  "create-comments": runCreateComments,
  "scenario-comment-nav": runScenarioCommentNav
};

function printHelp() {
  console.log(`Usage:\n  node scripts/dev-generate.mjs <command> [options]\n\nCommands:\n  create-clubs\n  create-projects\n  create-milestones\n  create-tasks\n  create-comments\n  scenario-comment-nav\n\nCommon options:\n  --actor <userId>\n  --count <n>\n  --prefix <label prefix>\n  --delay-ms <ms>\n`);
}

async function run() {
  const handler = handlers[command];
  if (!handler) {
    printHelp();
    if (command !== "help") {
      throw new Error(`Unknown command: ${command}`);
    }
    return;
  }
  await ensureBackendHealthy();
  const result = await handler();
  console.log(JSON.stringify(result, null, 2));
}

run().catch((error) => {
  console.error(`[dev-generate] ${error.message}`);
  process.exit(1);
});
